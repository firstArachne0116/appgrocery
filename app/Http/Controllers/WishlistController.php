    public function store(Request $request)
    {
        $wish = new Wishlist;
        $wish->product_id            = $request->product_id;
        $wish->productconfig_id      = $request->productconfig_id;
        $wish->user_id               = $request->user_id;
        $wish->status                = '1';
        $wish->created_at            = date('Y-m-d H:i:s');
        $wish->save();
        
